package demo.modelo;

public class Resultado {
	
	Integer valor;
	
	/**
	 * 
	 */
	public Resultado() {
		super();
	}

	/**
	 * @param valor
	 */
	public Resultado(Integer valor) {
		super();
		this.valor = valor;
	}

	/**
	 * @return the valor
	 */
	public Integer getValor() {
		return valor;
	}

	/**
	 * @param valor the valor to set
	 */
	public void setValor(Integer valor) {
		this.valor = valor;
	}
	
	

}
