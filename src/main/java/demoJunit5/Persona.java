package demoJunit5;

public class Persona {

	String name;
	int edad;

	public Persona(String name, int edad) {
		super();
		this.name = name;
		this.edad = edad;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

}
